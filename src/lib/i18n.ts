/** Reads `lang` cookie → all routes using the root layout are dynamically rendered (Next.js data access). */
export async function getLocale(): Promise<Locale> {
  const jar = await cookies();
  const raw = jar.get("lang")?.value;
  return raw === "en" ? "en" : "tr";
}

export function t<L extends Locale>(locale: L) {
  return DICT[locale];
}

export function formatFoundCount(template: string, count: number) {
  return template.replace(/\{count\}/g, String(count));
}

export function formatSeriesFound(locale: "tr" | "en", count: number) {
  return formatFoundCount(t(locale).topBar.seriesFound, count);
}

export function formatNameHeading(template: string, name: string) {
  return template.replace(/\{name\}/g, name);
}
